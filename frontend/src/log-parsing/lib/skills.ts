export function getClassIdFromSkillId(id: string | number) {
  if (typeof id === "number") id = `${id}`;

  let classId = 0;
  Object.entries(skills).every((entry) => {
    const [key, value] = entry;
    if (id in value) {
      classId = parseInt(key);
      return false;
    }
    return true;
  });

  return classId;
}

